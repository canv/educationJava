// 1 глава Основы java 46 стр (Упражнение 1.1 переводы из галлонов в литры)


public class GalToLit {

        public static void main (String args[]) {

            double gallons = 10;
            // lit - литры

            double lit = gallons * 3.7854;
            // gallons - галлоны
            // in one lit 3.7854 gallons

            System.out.println(" В " + gallons + " галлонах - " + lit + " литров");

    }


}
